    // carregar assinaturas salvos
    private void carregarAssinaturas() {
        listaModelAssinaturaDigitals = controllerAssinaturaDigital.getListaAssinaturaDigitalController();
        DefaultTableModel modelo = (DefaultTableModel) jtAssinaturas.getModel();
        modelo.setNumRows(0);
        String assinatura = "";
        //CARREGA OS DADOS DA LISTA NA TABELA
        int cont = listaModelAssinaturaDigitals.size();
        for (int i = 0; i < cont; i++) {
            assinatura = controllerEmpresa.getEmpresaController(listaModelAssinaturaDigitals.get(i).getEmpresa()).getEmpresa().getRazaoSocial();
            modelo.addRow(new Object[]{
                listaModelAssinaturaDigitals.get(i).getCodigo(),
                assinatura,
                listaModelAssinaturaDigitals.get(i).getVerproc(),
                listaModelAssinaturaDigitals.get(i).getIdtoken()
            });
        }
    }

    private void listarEmpresas() {
        try {
            listaEmpresas = controllerEmpresa.getListaEmpresaController();
        } catch (Exception ex) {
            Logger.getLogger(ViewParAssDigital.class.getName()).log(Level.SEVERE, null, ex);
        }
        jcbCodigoEmpresa.removeAllItems();
        jcbNomeEmpresa.removeAllItems();
        for (int i = 0; i < listaEmpresas.size(); i++) {
            jcbCodigoEmpresa.addItem(listaEmpresas.get(i).getCodigo() + "");
            jcbNomeEmpresa.addItem(listaEmpresas.get(i).getRazaoSocial());
        }
    }


