    //excluir um banco selecionado
    private void excluir() {
        int linha = jtAssinaturas.getSelectedRow();
        String nome = (String) jtAssinaturas.getValueAt(linha, 2);
        int codigo = (Integer) jtAssinaturas.getValueAt(linha, 0);
        //pegunta se realmente deseja excluir
        int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja"
                + " excluir o registro \nNome: "
                + nome + " ?", "Atenção", JOptionPane.YES_NO_OPTION);
        //se sim exclui, se não não faz nada    
        if (opcao == JOptionPane.OK_OPTION) {
            if (controllerAssinaturaDigital.excluirAssinaturaDigitalController(codigo)) {
                JOptionPane.showMessageDialog(this, "Registro excluido com suscesso!");
                carregarAssinaturas();
            } else {
                JOptionPane.showMessageDialog(this, "Você não pode excluir uma que possui vinculos!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    //salvar alterações
    private boolean alterar() {
        modelAssinaturaDigital = new ModelAssinaturaDigital();
        modelAssinaturaDigital.setCodigo(Integer.parseInt(jtfCodigo.getText()));
        modelAssinaturaDigital.setEmpresa(Integer.parseInt(jcbCodigoEmpresa.getSelectedItem().toString()));
        modelAssinaturaDigital.setSenhaTruststore(jtfSenhaTruststore.getText());
        modelAssinaturaDigital.setSenhaToken(jtfSenhaToken.getText());
        modelAssinaturaDigital.setSenhaKeystore(jtfSenhaKeystore.getText());
        modelAssinaturaDigital.setSerie55(jtfSerie55.getText());
        modelAssinaturaDigital.setSerie65(jtfSerie65.getText());
        modelAssinaturaDigital.setVerproc(jtfVersao.getText());
        modelAssinaturaDigital.setIdtoken(jtfIDToken.getText());
        modelAssinaturaDigital.setCsc(jtfCSCHomologacao.getText());
        modelAssinaturaDigital.setCscProd(jtfCSCProducao.getText());

        //alterar 
        if (controllerAssinaturaDigital.atualizarAssinaturaDigitalController(modelAssinaturaDigital)) {
            JOptionPane.showMessageDialog(this, "Registro alterado com sucesso!");
            this.limparCampos();
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar os dados!", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

