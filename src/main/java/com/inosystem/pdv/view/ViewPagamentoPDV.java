    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        //verifica se o valor recebido é o suficiente para pagar
        float ptroco;
        try {
            this.desconto = Float.parseFloat(this.jtfDesconto.getText());
        } catch (Exception e) {
            this.desconto = 0;
        }
        try {
            this.valorRecebido = Float.parseFloat(this.jtfValorRecebido.getText());
        } catch (Exception e) {
            this.valorRecebido = 0;
        }
        try {
            this.cartao = Float.parseFloat(this.jtfCartao.getText());
        } catch (Exception e) {
            this.cartao = 0;
        }
        try {
            this.cheque = Float.parseFloat(this.jtfCheque.getText());
        } catch (Exception e) {
            this.cheque = 0;
        }
        try {
            this.convenio = Float.parseFloat(this.jtfConvenio.getText());
        } catch (Exception e) {
            convenio = 0;
        }
        try {
            this.dinheiro = Float.parseFloat(this.jtfDinheiro.getText());
        } catch (Exception e) {
            dinheiro = 0;
        }
        try {
            ptroco = Float.parseFloat(jtfTroco.getText());
            if (ptroco < 0) {
                JOptionPane.showMessageDialog(null, "Valor recebido não é o suficiente para pagar a venda!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            } else {
                this.troco = ptroco;
                this.totalPagar = Float.parseFloat(this.jlfValorPagar.getText());
                this.tipoPagamento = "A vista";
                this.dinheiro = dinheiro - troco;
                doClose(RET_OK);
            }
        } catch (Exception e) {
            ptroco = 0;
            JOptionPane.showMessageDialog(null, "Informe o valor pago!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_okButtonActionPerformed

