    //verificar permissão do usuário
    public boolean retornarCodigoUsuarioLogado() {
        try {
            String permissao = new ControllerPermissaousuario().getPermissaousuarioCodUsuController(new ModelSessaoUsuario().codigo).getPermissao();
            if (permissao.equals("compras")) {
                return true;
            } else {
                return false;
            }
        } catch (Exception e) {
            return false;
        }
    }

    private float calcularValorRecebido() {
        float pdinheiro, pcartao, pcheque, pconvenio = 0;
        if (this.jtfDinheiro.getText().equals("")) {
            pdinheiro = 0;
        } else {
            pdinheiro = Float.parseFloat(this.jtfDinheiro.getText());
        }
        if (this.jtfCartao.getText().equals("")) {
            pcartao = 0;
        } else {
            pcartao = Float.parseFloat(this.jtfCartao.getText());
        }
        if (this.jtfCheque.getText().equals("")) {
            pcheque = 0;
        } else {
            pcheque = Float.parseFloat(this.jtfCheque.getText());
        }
        if (this.jtfConvenio.getText().equals("")) {
            pconvenio = 0;
        } else {
            pconvenio = Float.parseFloat(this.jtfConvenio.getText());
        }
        return pdinheiro + pcartao + pcheque + pconvenio;
    }

    private float calcularDesconto() {
        float pSubTotal, pDesconto = 0;
        if (this.jtfSubtotal.getText().equals("")) {
            pSubTotal = 0;
        } else {
            pSubTotal = Float.parseFloat(this.jtfSubtotal.getText());
        }
        if (this.jtfDesconto.getText().equals("")) {
            pDesconto = 0;
        } else {
            pDesconto = Float.parseFloat(this.jtfDesconto.getText());
        }
        valorTotal = pSubTotal - pDesconto;
        return valorTotal;
    }

    private float calcularTroco() {
        float pValorRecebid = 0;
        if (this.jtfValorRecebido.getText().equals("")) {
            pValorRecebid = 0;
        } else {
            pValorRecebid = Float.parseFloat(this.jtfValorRecebido.getText());
        }
        return pValorRecebid - valorTotal;
    }

