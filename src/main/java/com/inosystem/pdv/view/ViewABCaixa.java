    private void jbAbrirFecharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAbrirFecharActionPerformed
        // TODO add your handling code here:
        if (jcbCaixas.getSelectedIndex() == 0) {
            //selecione um caixa   
            JOptionPane.showMessageDialog(this, "Você deve selecionar o número do caixa!");
        } else {
            modelCaixa = new Caixa();
            //Abrir ou fechar um caixa
            modelCaixa = controllerCaixa.verificarRetorarCaixaControler(getNumeroCaixa());

            if (modelCaixa.getStatus() == 0) {
                //não tem caixa aberto - abrir caixa
                modelCaixa.setCaixaNumero(getNumeroCaixa());
                modelCaixa.setDinheiro(Double.parseDouble(jtfDinheiro.getText()));
                modelCaixa.setCartao(Double.parseDouble(jtfCartao.getText()));
                modelCaixa.setCheque(Double.parseDouble(jtfCheque.getText()));
                modelCaixa.setConvenio(Double.parseDouble(jtfVale.getText()));
                modelCaixa.setCodigoUsuario(SessaoUsuario.codigo);
                modelCaixa.setStatus(1);
                try {
                    modelCaixa.setDataAbertura(bLMascaras.converterDataParaDateUS(jtfDataAbertura.getDate()));
                } catch (Exception ex) {
                    Logger.getLogger(ViewABCaixa.class.getName()).log(Level.SEVERE, null, ex);
                }
                // salvar novo caixa
                if (modelCaixa.getCodigo() == 0) {
                    if (controllerCaixa.salvarCaixaController(modelCaixa) > 0) {
                        JOptionPane.showMessageDialog(this, "Abertura de caixa realizada com sucesso!", "ATENÇÂO", JOptionPane.WARNING_MESSAGE);
                        viewPdv.setNumeroCaixa(jcbCaixas.getSelectedIndex());
                        viewPdv.setVisible(true);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro!", "ERRO", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    //reabrir o caixa
                    if (controllerCaixa.atualizarCaixaController(modelCaixa)) {
                        JOptionPane.showMessageDialog(this, "Abertura de caixa realizada com sucesso!", "ATENÇÂO", JOptionPane.WARNING_MESSAGE);
                        viewPdv.setNumeroCaixa(jcbCaixas.getSelectedIndex());
                        viewPdv.setVisible(true);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro!", "ERRO", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                //tem caixa aberto - fechar caixa
                Object opcoes[] = {"Ir para PDV", "Fechar Caixa", "Cancelar"};
                int op = JOptionPane.showOptionDialog(null, "Você deseja fechar o caixa ou ir para o PDV", "O caixa está aberto", 1, 3, null, opcoes, null);
                switch (op) {
                    case 1:
                        //quero fechar o caixa
                        modelCaixa.setStatus(0);
                        try {
                            modelCaixa.setDataAbertura(bLMascaras.converterDataParaDateUS(jtfDataAbertura.getDate()));
                            modelCaixa.setDataFechamento(bLMascaras.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
                        } catch (Exception ex) {
                            Logger.getLogger(ViewABCaixa.class.getName()).log(Level.SEVERE, null, ex);
                        }   // salvar novo caixa   // salvar novo caixa
                        if (controllerCaixa.atualizarCaixaController(modelCaixa)) {
                            JOptionPane.showMessageDialog(this, "Fechamento de caixa realizado com sucesso!", "ATENÇÂO", JOptionPane.WARNING_MESSAGE);
                            carregarCaixa();
                            limparTela();
                        } else {
                            JOptionPane.showMessageDialog(this, "Erro!", "ERRO", JOptionPane.ERROR_MESSAGE);
                        }
                        break;
                    case 0:
                        //quero continuar com caixa aberto
                        //setar o numero do caixa e fechar
                        viewPdv.setNumeroCaixa(jcbCaixas.getSelectedIndex());
                        viewPdv.setNumeroCaixaLabel(jcbCaixas.getSelectedIndex());
                        viewPdv.setVisible(true);
                        dispose();
                        break;
                    case 2:
                        dispose();
                        break;
                    default:
                        break;
                }
            }
        }
    }//GEN-LAST:event_jbAbrirFecharActionPerformed

