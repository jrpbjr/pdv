    private void carregarCaixa() {
        // TODO add your handling code here:
        modelCaixa = new Caixa();
        setNumeroCaixa(jcbCaixas.getSelectedIndex());
        //verifica se o caixa ta aberto se tiver retorna os dados
        modelCaixa = controllerCaixa.verificarRetorarCaixaControler(getNumeroCaixa());
        if (modelCaixa.getStatus() == 0) {
            // não tem caixa aberto
            //cadastrar novo caixa
            try {
                jtfDataAbertura.setDate(bLMascaras.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
            } catch (Exception ex) {
                Logger.getLogger(ViewABCaixa.class.getName()).log(Level.SEVERE, null, ex);
            }
            jtfDinheiro.setText("0");
            jtfCartao.setText("0");
            jtfCheque.setText("0");
            jtfVale.setText("0");
            habilitarCampos(true);
            jlStatus.setText("Caixa fechado");
            jbAbrirFechar.setText("Abrir caixa");

        } else {
            //tem caixa aberto
            //alterar caixa
            habilitarCampos(false);
            jtfDataAbertura.setDate(modelCaixa.getDataAbertura());
            jtfDinheiro.setText(bLMascaras.arredondamentoDoubleComPontoDuasCasasString(modelCaixa.getDinheiro()));
            jtfCartao.setText(bLMascaras.arredondamentoDoubleComPontoDuasCasasString(modelCaixa.getCartao()));
            jtfCheque.setText(bLMascaras.arredondamentoDoubleComPontoDuasCasasString(modelCaixa.getCheque()));
            jtfVale.setText(bLMascaras.arredondamentoDoubleComPontoDuasCasasString(modelCaixa.getConvenio()));
            somarValorTotal();
            jlStatus.setText("Caixa aberto");
            jbAbrirFechar.setText("Fechar ou PDV");
        }
    }

    private void somarValorTotal() {
        double dinheiro = 0, cartao = 0, cheque = 0, convenio = 0;
        try {
            dinheiro = Double.parseDouble(jtfDinheiro.getText());
        } catch (Exception e) {
            dinheiro = 0;
        }
        try {
            cartao = Double.parseDouble(jtfCartao.getText());
        } catch (Exception e) {
            cartao = 0;
        }
        try {
            cheque = Double.parseDouble(jtfCheque.getText());
        } catch (Exception e) {
            cheque = 0;
        }
        try {
            convenio = Double.parseDouble(jtfVale.getText());
        } catch (Exception e) {
            convenio = 0;
        }
        jtfValorTotal.setText(bLMascaras.arredondamentoDoubleComPontoDuasCasasString(dinheiro + cartao + cheque + convenio));

    }

    private void habilitarCampos(boolean status) {
        jtfDinheiro.setEnabled(status);
        jtfCartao.setEnabled(status);
        jtfCheque.setEnabled(status);
        jtfVale.setEnabled(status);
    }

    private void carregarConfiguracoes() {
        jlUsuario.setText(SessaoUsuario.nome);
        AtualizadorHorario ah = new AtualizadorHorario(jlDataHora);
        ah.mostrarData(true);
        Thread thHora = ah;
        thHora.start();
        setNumeroCaixa(jcbCaixas.getSelectedIndex());
    }

