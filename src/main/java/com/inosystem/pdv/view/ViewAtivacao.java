    private Config getDadosInterface() {
        Config config = new Config();

        String setialComTralha = this.jtfSerial.getText();
        String serial[] = setialComTralha.split("#");

        config.setSoftware(serial[0]);
        config.setDataGeracaoStr(serial[1]);
        config.setDataAtualStr(serial[2]);
        config.setDiasValidadeStr(serial[3]);
        config.setNomePC(serial[4]);


        return config;
    }


    /**
     * gera o arquivo config.xml
     *
     * @param pConfing
     * @return boolean
     * @throws IOException
     */
    private boolean gerarArquivo(Config pConfing) throws IOException {
        String textoArquivo = new String();
        XStream xStream = new XStream();
        FileWriter fileWriter = new FileWriter("config/licenca.xml");
        PrintWriter printWriter = new PrintWriter(fileWriter);

        xStream.alias("licenca", Config.class);
        //crio o arquivo xml
        textoArquivo = "<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n";
        textoArquivo += xStream.toXML(pConfing);
        printWriter.printf(textoArquivo);
        fileWriter.close();
        return true;
    }


     public boolean imprimirLicenca() {
        Config config = new Config();
        XStream xStream = new XStream();
        CriptografiaReversivel criptografia = new CriptografiaReversivel();
        ConfigXML configXML = new ConfigXML();
        xStream.alias("licenca", Config.class);

        try {

//TODA VEZ QUE ABRIR O PROGRAMA
//testando se voltou a data do computador
            config = configXML.getConfig("config/licenca.xml");

            jtfSerial.setText(config.getSoftware()+"#"+config.getDataAtualStr()+"#"+config.getDataGeracaoStr()+"#"+config.getDiasValidadeStr()+"#"+config.getNomePC());
            
//descriptografando os dados
            config.setSoftware(criptografia.descriptografar(config.getSoftware()));
            config.setDataAtualStr(criptografia.descriptografar(config.getDataAtualStr()));
            config.setDataGeracaoStr(criptografia.descriptografar(config.getDataGeracaoStr()));
            config.setDiasValidadeStr(criptografia.descriptografar(config.getDiasValidadeStr()));
            config.setNomePC(criptografia.descriptografar(config.getNomePC()));

            jtfNomeComputador.setText(config.getNomePC());
            jtfNomeSoftware.setText(config.getSoftware());
            jtfDataGeracao.setText(config.getDataGeracaoStr());
            jtfDataAtual.setText(config.getDataAtualStr());
            jtfValidae.setText(config.getDiasValidadeStr());
            jtfDiasValidade.setText(config.getDiasValidadeStr());  
            
        } catch (Exception e) {
            System.err.println(e);
            JOptionPane.showMessageDialog(this, "Erro: Serial incorreto.");
            return false;
        }
        return true;
    }

